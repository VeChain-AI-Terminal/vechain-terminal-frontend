export async function upsertUserByAddress(address: string): Promise<User> {
  console.log("upsertUserByAddress", address);
  try {
    const addr = address.toLowerCase();
    const existing = await db
      .select()
      .from(user)
      .where(eq(user.address, addr))
      .limit(1);
    if (existing.length > 0) return existing[0];

    const newUser = {
      id: generateUUID(),
      address: addr,
    };

    const [created] = await db
      .insert(user)
      .values(newUser)
      .returning();
    return created;
  } catch (error) {
    console.error("Error in upsertUserByAddress:", error);
    throw new ChatSDKError(
      "bad_request:database",
      "Failed to upsert user by address"
    );
  }
}

